function uniqueValues(arr) {
    return arr.reduce((acc, item) => {
        if (!acc.includes(item)) {
            acc.push(item);
        }
        return acc;
    }, []);
}

console.log(uniqueValues([1, 2, 3, 2, 1, 4, 5, 4])); 
// [1, 2, 3, 4, 5]